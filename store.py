class Store:
    def __init__(self, name, address):
        self.name = name
        self.address = address
        self.items = {}  # —Å–ª–æ–≤–∞—Ä—å "—Ç–æ–≤–∞—Ä : —Ü–µ–Ω–∞"

    def add_item(self, item_name, price):
        """–î–æ–±–∞–≤–∏—Ç—å —Ç–æ–≤–∞—Ä –≤ –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç"""
